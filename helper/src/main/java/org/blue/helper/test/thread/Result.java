package org.blue.helper.test.thread;

public class Result {
    private String url;
    private Integer finishResult;

    public Result(String url, Integer finishResult) {
        this.url = url;
        this.finishResult = finishResult;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public Integer getFinishResult() {
        return finishResult;
    }

    public void setFinishResult(Integer finishResult) {
        this.finishResult = finishResult;
    }
}
