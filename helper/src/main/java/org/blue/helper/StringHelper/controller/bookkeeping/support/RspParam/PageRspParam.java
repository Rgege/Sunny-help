package org.blue.helper.StringHelper.controller.bookkeeping.support.RspParam;

import java.util.List;

public class PageRspParam <T>{
    private Integer total;  //总数量
    private Integer size;   //当前页的数量
    private Integer currentPage; //当前页
    private List<T> cotentList; //内容

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public Integer getCurrentPage() {
        return currentPage;
    }

    public void setCurrentPage(Integer currentPage) {
        this.currentPage = currentPage;
    }

    public List<T> getCotentList() {
        return cotentList;
    }

    public void setCotentList(List<T> cotentList) {
        this.cotentList = cotentList;
    }
}
